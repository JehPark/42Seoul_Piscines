int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	if (n == 0)
		return (0);
	i = 0;
	while (i < n && *s1 && *s2)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		s2++;
		s1++;
		i++;
	}
	if (i == n)
	{
		s1--;
		s2--;
	}
	return (*s1 - *s2);
}
